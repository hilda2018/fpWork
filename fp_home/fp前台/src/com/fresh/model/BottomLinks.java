package com.fresh.model;

import java.io.Serializable;

import com.nova.frame.annotation.Id;
import com.nova.frame.annotation.Table;

@Table("bottomlinks")
public class BottomLinks implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	
	/**
	 * 底部链接ID
	 */
	@Id(auto = false)
	private String bottomlinksid;
	/**
	 * 底部链接名称
	 */
	private String bottomlinksname;
	/**
	 * 底部链接链接地址
	 */
	private String bottomlinksaddress;
	/**
	 * 类型
	 */
	private String bottomlinkstype;
	/**
	 * 显示顺序
	 */
	private String indexsortshow;
	
	public String getBottomlinksid() {
		return bottomlinksid;
	}
	public void setBottomlinksid(String bottomlinksid) {
		this.bottomlinksid = bottomlinksid;
	}
	public String getBottomlinksname() {
		return bottomlinksname;
	}
	public void setBottomlinksname(String bottomlinksname) {
		this.bottomlinksname = bottomlinksname;
	}
	public String getBottomlinksaddress() {
		return bottomlinksaddress;
	}
	public void setBottomlinksaddress(String bottomlinksaddress) {
		this.bottomlinksaddress = bottomlinksaddress;
	}
	public String getBottomlinkstype() {
		return bottomlinkstype;
	}
	public void setBottomlinkstype(String bottomlinkstype) {
		this.bottomlinkstype = bottomlinkstype;
	}
	public String getIndexsortshow() {
		return indexsortshow;
	}
	public void setIndexsortshow(String indexsortshow) {
		this.indexsortshow = indexsortshow;
	}

}
