package com.fresh.model;

import java.io.Serializable;

import com.nova.frame.annotation.Id;
import com.nova.frame.annotation.Table;

@Table("d_user")
public class DUser implements Serializable {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    @Id(auto = false)
    private String id;

    private String code;

    private String name;

    private String password;

    private String isusing;

    private String description;

    private String parentid;

    private String email;

    private String telnum;

    private String isapp;

    private String phonenum;

    private String nickname;

    private String defaultprintcount;

    private String ismoneyplan;

    private String contactperson;

    private String isregistration;// 是否是注册：0是，1不是

    private String isuserinsert;// 0运维添加 1客户添加

    public String getIsuserinsert() {
        return isuserinsert;
    }

    public void setIsuserinsert(String isuserinsert) {
        this.isuserinsert = isuserinsert;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getIsusing() {
        return isusing;
    }

    public void setIsusing(String isusing) {
        this.isusing = isusing;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getParentid() {
        return parentid;
    }

    public void setParentid(String parentid) {
        this.parentid = parentid;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTelnum() {
        return telnum;
    }

    public void setTelnum(String telnum) {
        this.telnum = telnum;
    }

    public String getIsapp() {
        return isapp;
    }

    public void setIsapp(String isapp) {
        this.isapp = isapp;
    }

    public String getPhonenum() {
        return phonenum;
    }

    public void setPhonenum(String phonenum) {
        this.phonenum = phonenum;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getDefaultprintcount() {
        return defaultprintcount;
    }

    public void setDefaultprintcount(String defaultprintcount) {
        this.defaultprintcount = defaultprintcount;
    }

    public String getIsmoneyplan() {
        return ismoneyplan;
    }

    public void setIsmoneyplan(String ismoneyplan) {
        this.ismoneyplan = ismoneyplan;
    }

    public String getContactperson() {
        return contactperson;
    }

    public void setContactperson(String contactperson) {
        this.contactperson = contactperson;
    }

    public String getIsregistration() {
        return isregistration;
    }

    public void setIsregistration(String isregistration) {
        this.isregistration = isregistration;
    }

}
