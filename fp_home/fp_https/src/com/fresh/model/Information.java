package com.fresh.model;

import java.io.Serializable;
import java.util.Date;

import com.nova.frame.annotation.Id;
import com.nova.frame.annotation.Table;

@Table("information")
public class Information implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = -6472669326209772653L;
	/**
	 * 资讯ID
	 */
	@Id(auto = false)
	private String _informationID;
	/**
	 * 资讯名称
	 */
	private String _informationName;
	/**
	 * 资讯来源
	 */
	private String _informationSource;
	/**
	 * 资讯日期
	 */
	private String _informationDate;
	/**
	 * 资讯类型
	 */
	private Integer _informationType;
	/**
	 * 资讯类型
	 */
	private String _IsLink;
	/**
	 * 时间戳
	 */
	private String _currentDate;
	
	public String getinformationid() {
		return _informationID;
	}

	public void setinformationid(String _informationID) {
		this._informationID = _informationID;
	}
	
	public String getinformationname() {
		return _informationName;
	}

	public void setinformationname(String _informationName) {
		this._informationName = _informationName;
	}
	
	public String getinformationsource() {
		return _informationSource;
	}

	public void setinformationsource(String _informationSource) {
		this._informationSource = _informationSource;
	}
	
	public String getinformationdate() {
		return _informationDate;
	}

	public void setinformationdate(String _informationDate) {
		this._informationDate = _informationDate;
	}
	
	public Integer getinformationtype() {
		return _informationType;
	}

	public void setinformationtype(Integer _informationType) {
		this._informationType = _informationType;
	}
	
	public String getislink() {
		return _IsLink;
	}

	public void setislink(String _IsLink) {
		this._IsLink = _IsLink;
	}
	
	public String getcurrentdate() {
		return _currentDate;
	}

	public void setcurrentdate(String _currentDate) {
		this._currentDate = _currentDate;
	}
}
