package com.fresh.common.base.bean;


public class AllImgBean{
	/**
	 * 图片名字
	 */
	private String imgName;
	//由类似如下地址组成：/shops/product/1/
	private String imgPath;
	/**
	 * 大图片
	 */
	private String bigImgUrl;
	/**
	 * 中图片
	 */
	private String middleImgUrl;
	/**
	 * 超大图片
	 */
	private String hugeImgUrl;

	public void setImgName(String imgName) {
		this.imgName = imgName;
	}

	public String getImgName() {
		return imgName;
	}

	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}

	public String getImgPath() {
		return imgPath;
	}

	public void setBigImgUrl(String bigImgUrl) {
		this.bigImgUrl = bigImgUrl;
	}

	public String getBigImgUrl() {
		return bigImgUrl;
	}

	public void setMiddleImgUrl(String middleImgUrl) {
		this.middleImgUrl = middleImgUrl;
	}

	public String getMiddleImgUrl() {
		return middleImgUrl;
	}

	public void setHugeImgUrl(String hugeImgUrl) {
		this.hugeImgUrl = hugeImgUrl;
	}

	public String getHugeImgUrl() {
		return hugeImgUrl;
	}
}
